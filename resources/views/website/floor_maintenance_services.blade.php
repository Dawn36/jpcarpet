    <h4>So what is Strip and Wax?</h4>
    <p>Strip and Wax is exactly what the name suggests. Some flooring, especially those for hospitals and doctors offices, is waxed, and several months after the layer of wax is applied, it begins to wear down. This is normal and expected considering the amount of foot traffic where this flooring type is used. But just because it is to be expected, doesn't not mean it needs to be accepted. Once the wax layer starts wearing out, that's where we come in. We remove the previous layer of wax, along with all the dirt, dust, and scuffs collected on that wax, steam clean the floor underneath to ensure a completely clean floor, then we carefully apply the wax.</p>
