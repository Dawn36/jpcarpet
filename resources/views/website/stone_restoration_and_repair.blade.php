                  <p>Travertine is a sedimentary rock that originated as limestone (calcium carbonate) and changed over time due to extreme heat and pressure from the earthâ€™s core. Holes and voids were created throughout the stone layers when pressurized hot water and steam were forced through the rock during formation and the cooling process, thus creating the beautiful veins travertine is known for.Travertine comes in a variety of colors from grey to coral-red, and is most commonly available in tile sizes for floor installations. </p>
                  <p> Travertine is one of several natural stones that are used for paving patios and garden paths. It is sometimes known as travertine limestone or travertine marble; these are the same stone, although travertine is classified properly as a type of limestone, not marble. The stone is characterized by pitted holes and troughs in its surface. </p>
                  <p> Although these troughs occur naturally, they suggest signs of considerable wear and tear. Along with filling these troughs to ensure they don't grow larger over time, travertine can be polished. Depending upon the type of travetine you have, it may be polishing to either a mirror-like finish, or a beautiful natural look. JP Floor Care takes care to fill the pits in your travertine to match your stone, so once dried and polished, you wouldn't be able to tell where the hole used to be. We strive to provide you the highest of quality for your travertine repair and polish. </p>
