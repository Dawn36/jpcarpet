<div class="container">
  <h3>What Should I Do When Water Damage Happens?</h3>
  <ul>
  <li>Find where the water is leaking from. If possible, stop the leak or call someone who can.</li>
  <li>Turn off all electrical appliances in and around the area to reduce electrical hazards.</li>
  <li>Remove furniture and other items off wet areas to prevent permanent stains or rust.</li>
  <li>Empty out closet floors, including shoes and cardboard boxes which cause staining.</li>
  </ul>
</div>
<div class="container pb-15">
  <h3>What Should I Avoid Doing When Water Damage Happens?</h3>
  <ul>
  <li>Never turn on the ceiling or wall fixtures if it’s wet.</li>
  <li>Never rip up the carpet and pad on your own, always leave this to a professional.</li>
  <li>Do not try to dry carpeting with an electric heater.</li>
  <li>Never leave books, newspapers and other materials on wet carpets and floors.</li>
  <li>Do not turn up the heat above 68 degrees, as this will facilitate mildew growth and possibly discolor your carpet.</li>
  </ul>
