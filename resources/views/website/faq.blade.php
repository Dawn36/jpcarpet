		<div class="row">
      	<div class="anim-section mb-30">
          <div class="panel-body">
                	  <h4 class="panel-title">Q: What Method Do You Use For Cleaning?</h4>
                  <p> A: The method we use for cleaning is hot water extraction, commonly referred to as steam cleaning or hot water extraction. Soft water and a specially formulated cleaning solution are injected deep within the carpet fibers. Then, a powerful suction extracts deep-down dirt, allergens, cleaning solution and 95% of the moisture. It’s the cleaning method most recommend by carpet manufacturers. </p>
                </div>
          <div class="panel-body">
                	  <h4 class="panel-title">Q: Do I Need To Vacuum Before The Crew Arrives?</h4>
                  <p> A: A light vacuuming in the traffic areas is recommended and appreciated but not required. If needed, the crew can vacuum the area to be cleaned. Also, the equipment is vacuuming the entire time we are cleaning. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Do You Move Furniture?</h4>
                  <p> A: Yes. There is no charge to move furniture. We are professionally trained to move your furniture. After the cleaning is completed, we will return it to its rightful place on protective padding as needed. We do suggest you remove items off your furniture, like electronics, plants, lamps, and other small items. We will move most furniture that can safely move. There are several items we will not move, including pianos, electronics, china cabinets, beds, dressers with mirrors, pool tables, etc.</p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: How Long Will It Take For My Carpet To Dry?</h4>
                  <p> A: Dry time affects by humidity, temperature, and air flow as well as whether the carpet was cleaned with a portable unit or truck-mounted unit. It can take anywhere between 4-8 hours for the carpet to completely dry. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: When Can I Remove The Protective Blocks?</h4>
                  <p> A: You need to make sure the carpet is 100% dry to avoid any color transfer. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Can I Walk On The Carpet Right After Carpet Cleaning?</h4>
                  <p> A: You may walk on your carpet immediately after cleaning, but avoid wearing street shoes, as they may re-soil your carpet. Be very careful when walking from steam clean carpeted areas on to hard surface areas to avoid slipping. </p>
                </div>
			<div class="panel-body">
                	  <h4 class="panel-title">Q: After Cleaning My Carpet Some Spots Have Reappeared. What Should I Do?</h4>
                  <p> A: Our service is 100% guarantee, call us immediately! We will take care of it. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: How Do You Guarantee Customer Satisfaction?</h4>
                  <p> A: We are happy to work with you to correct any situation that may arise. In some cases, this can mean spot cleaning specific areas of concern or even completely re-cleaning the area in question. In the end, we’re not satisfied until you are. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Should I Apply A Protector To My Cleaned Carpets Or Upholstery?</h4>
                  <p> A: Carpet or upholstery protector can help carpet and upholstery resist soiling from dry dirt, water- and oil-based spills by wrapping each fiber in a protective coating. This extra layer gives you more time to clean up spots and spills before they soak into the fibers, helping you keep your carpet and upholstery looking its best between professional cleanings. Plus, it makes vacuuming and cleaning up spots easier.</p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: How Often Do Manufacturers Recommend Professional Cleaning?</h4>
                  <p> A: Manufacturers recommend having upholstered furniture professionally cleaned every 12-24 months depending on use </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Why Should I Have My Tile & Grout Cleaned?</h4>
                  <p> A: Just like carpet, dirt builds up over time dulling your tile’s appearance and discoloring your grout. A thorough, professional tile & grout cleaning is the only way to remove the deep-down dirt. </p>
					 <p>A: We use a high-pressure rotary tool with dual rotating jets. This is combined with the same truck-mounted steam/pressure cleaning we use to deep clean your carpet. We use tile and grout specific cleaners that strip away any waxy build-up and remove the dirt embedded in the grout lines.</p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: How Often Should I Have My Floor Cleaned?</h4>
                  <p> A: As with carpet, many factors must be considered when determining the frequency of cleaning. Foot traffic, children, pets, and routine floor maintenance will affect how often you need to have your tile & grout cleaned. </p>
                </div>
			<div class="panel-body">
                	  <h4 class="panel-title">Q: How Long Will It Take To Have My Tile Floor Cleaned?</h4>
                  <p> A: For an average size room (up to 300 square feet), it typically takes 30–60 minutes to clean the tile & grout. This can vary based on a number of factors including the size of the tile, amount of soil in the grout lines, and room configuration. If you choose to have sealant added, it typically takes 2–3 hours to complete the entire process. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Why Should I Have My Grout Lines Sealed?</h4>
                  <p> A: Grout is a porous material that absorbs soil and spills, just like carpet. Having a sealant applied will help create a protective barrier, giving you more time to clean up dirt and liquid spills before they soak into the grout. The type of sealant you choose will determine the degree of stain resistance. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: How Long Do I Have To Stay Off The Floor After The Sealant Has Been Applied?</h4>
                  <p> A: We recommend that you wait 30 minutes before walking on the floor to allow the sealant to dry. It will take 24 hours for the sealant to cure completely, so we suggest that you avoid any liquid coming in contact with the sealant during that time. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: Do I Have To Use A Special Floor Cleaner If I Have The Sealant Applied?</h4>
                  <p> A: To preserve the life of the sealant, we recommend that you use a pH neutral cleaner on your floor. Rinsing after mopping will also help keep your floors looking their best. You will want to avoid bleach and other harsh chemical cleaners as these can wear away at the sealant and cause it to break down prematurely. </p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: What Is The Clear Seal?</h4>
                  <p>A: The Clear seal is an invisible layer of protection that penetrates into the pores of the grout. This gives you more time to clean up dirt and liquid spills before they soak into the grout, however, it is not waterproof.</p>
                </div>
                 <div class="panel-body">
                	  <h4 class="panel-title">Q: What Is A Color Seal?</h4>
                  <p> A: Restore your grout to its original appearance with our premium Color Seal restoration service. Our Color Seal service will transform your discolored grout back to its original splendor by providing a layer of uniform color to your grout lines, giving your floor a clean and refreshed look. Along with improving the cosmetic appearance of your floor, Color Seal provides a protective waterproof barrier that guards your grout against spills, dirt & debris. </p>
                </div>
			<div class="panel-body">
                	  <h4 class="panel-title">Q: How Do You Clean Hardwood Floors?</h4>
                  <p> A: Regular mopping just doesn’t reach the stubborn dirt that lies deep in the pores of your hardwood, but our cleaning process does. This process includes an initial vacuuming of the areas to remove any loose dirt and debris, an auto scrubbing with an alkaline cleaner and then another auto scrubbing with a pH neutral cleaner to return the floor to its initial sheen. Edges are hand scrubbed, as are stairs. </p>
